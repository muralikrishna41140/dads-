type TranslationTree = typeof TRANSLATIONS.en;
type TranslateArgs = Record<string, string | number>;

const LanguageContext = createContext<{
  language: Language;
  setLanguage: (language: Language) => void;
  t: (path: string, values?: TranslateArgs) => string;
} | null>(null);

function getValue(tree: TranslationTree, path: string): unknown {
  return path.split(".").reduce<unknown>((current, key) => {
    if (current && typeof current === "object" && key in current) {
      return (current as Record<string, unknown>)[key];
    }
    return undefined;
  }, tree);
}

function format(template: string, values?: TranslateArgs) {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ""));
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    if (typeof window === "undefined") return "en";
    const saved = window.localStorage.getItem(STORAGE_KEY);
    return saved === "hi" || saved === "te" || saved === "en" ? saved : "en";
  });

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.documentElement.dir = "ltr";
  }, [language]);

  const value = useMemo(() => {
    const t = (path: string, values?: TranslateArgs) => {
      const translated =
        getValue(TRANSLATIONS[language], path) ?? getValue(TRANSLATIONS.en, path) ?? path;
      return typeof translated === "string" ? format(translated, values) : path;
    };

    return { language, setLanguage, t };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useI18n() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useI18n must be used within a LanguageProvider");
  }
  return context;
}

